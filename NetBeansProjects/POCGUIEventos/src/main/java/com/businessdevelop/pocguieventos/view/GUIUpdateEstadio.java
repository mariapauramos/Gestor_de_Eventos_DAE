    private void jButtonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActualizarActionPerformed
        String idEstadio = txtIdEstadio.getText().trim();

        if (idEstadio.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Primero busque un estadio.");
            return;
        }

        Estadio estadio = servicioEstadio.searchEstadio(idEstadio);

        if (estadio != null) {
            try {
                estadio.setNombre(txtNombre.getText().trim());
                estadio.setCapacidad(Integer.parseInt(txtCapacidad.getText().trim()));
                java.util.Date fechaSeleccionada = jDateChooserFecha.getDate();
                if (fechaSeleccionada != null) {
                    estadio.setFechaInauguracion(
                        fechaSeleccionada.toInstant()
                                         .atZone(java.time.ZoneId.systemDefault())
                                         .toLocalDate()
                    );
                } else {
                    JOptionPane.showMessageDialog(this, "Seleccione una fecha de inauguración.");
                    return;
                }
                estadio.setPropietario(txtPropietario.getText().trim());
                estadio.setNumeroGradas(Integer.parseInt(txtNumeroGradas.getText().trim()));
                estadio.setNumeroPalcosvip(Integer.parseInt(txtNumeroPalcos.getText().trim()));

                Estadio update = servicioEstadio.updateEstadio(estadio);

                if (update!=null) {
                    JOptionPane.showMessageDialog(this, "Estadio actualizado correctamente.");
                    habilitarCampos(false);
                    // Limpiar los campos después de eliminar
                    txtIdEstadio.setText("");
                    txtNombre.setText("");
                    txtCapacidad.setText("");
                    jDateChooserFecha.setDate(null);
                    txtPropietario.setText("");
                    txtNumeroGradas.setText("");
                    txtNumeroPalcos.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "No se pudo actualizar el estadio.", "Error", JOptionPane.ERROR_MESSAGE);
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error en los datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe buscar un estadio válido.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonActualizarActionPerformed

