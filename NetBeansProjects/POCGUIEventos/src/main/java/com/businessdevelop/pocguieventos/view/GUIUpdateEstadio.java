    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed

        String idEstadio = txtIdEstadio.getText().trim();
        if (idEstadio.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un ID de evento.");
            return;
        }

        Estadio estadio = servicioEstadio.searchEstadio(idEstadio);

        if (estadio != null && estadio instanceof Estadio) {
            Estadio et = (Estadio) estadio;

            // Llenamos los campos
            txtNombre.setText(et.getNombre());
            txtCapacidad.setText(String.valueOf(et.getCapacidad()));
            // Formatear fecha
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            java.util.Date date = java.util.Date.from(
            et.getFechaInauguracion().atStartOfDay(java.time.ZoneId.systemDefault()).toInstant());
            jDateChooserFecha.setDate(date);
            txtPropietario.setText(et.getPropietario());
            txtNumeroGradas.setText(String.valueOf(et.getNumeroGradas()));
            txtNumeroPalcos.setText(String.valueOf(et.getNumeroPalcosvip()));

        } else {
            JOptionPane.showMessageDialog(this, "Estadio no existe.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonBuscarActionPerformed

